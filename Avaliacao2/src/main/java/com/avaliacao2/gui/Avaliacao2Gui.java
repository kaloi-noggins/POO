    private void myInitComponents() {
        Leitor leitor = new Leitor();
        Palindromo palindromo = new Palindromo();
        
        String[] lines = leitor.getLines();
        
        jList1.setModel(new javax.swing.AbstractListModel<String>() {
            //Leitura das linhas do arquivo
            String[] strings = lines;
            @Override
            public int getSize() { return strings.length; }
            @Override
            public String getElementAt(int i) { return strings[i]; }
        });
        
        ArrayList<Integer> intArray = new ArrayList<>();
        
        for(int i = 0; i<lines.length; i++)
        {
            System.out.println(jList1.getModel().getElementAt(i));
            if(palindromo.isPalindromo(jList1.getModel().getElementAt(i)))
            {
               intArray.add(i);
            }
        }
        
        int[] indices = new int[intArray.size()];
        
        for(int i=0; i<intArray.size();i++)
        {
            indices[i] = intArray.get(i);
        }
        
        System.out.println(indices + " " + intArray );
        jList1.setSelectedIndices(indices);
    }
